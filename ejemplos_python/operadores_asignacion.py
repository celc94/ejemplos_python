#Operadores de Asignación
#Operador =
miVariable = 10
print(miVariable)

#Operador de Reasignación
#Aumentar el valor de la variable en 1
miVariable = miVariable + 1 
print(miVariable)

#Sintaxis resumida para incrementar el valor de la variable en el numero que se requiera +=
miVariable += 2
print(miVariable)
miVariable += 4
print(miVariable)

#Se puede reasignar valores con cualquier operador matemático
miVariable -= 7
print(miVariable)
miVariable *= 100
print(miVariable)
miVariable /= 10
print(miVariable)