a = 4
b = 2

#Se utiliza el == para preguntar una igualdad
resultado = a == b
print(f'Resultado == : {resultado}')

#Dos Variables distintas !=
resultado = a != b
print(f'Resultado != : {resultado}')

#Mayor que >
resultado = a > b
print(f'Resultado > : {resultado}')

#Menor que >
resultado = a < b
print(f'Resultado < : {resultado}')

#Mayor >= que
resultado = a >= b
print(f'Resultado >= : {resultado}')

#Menor <= que
resultado = a <= b
print(f'Resultado <= : {resultado}')