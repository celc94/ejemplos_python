#Operadores Aritméticos
#Suma
#Se utiliza float para poder aceptar número decimales
print('SUMA')
numero1 = float(input('Digite el primer número: '))
numero2 = float(input('Digite el segundo número: '))
suma = numero1 + numero2
#Utilizar f string para imprimir las variables, dichas se deben de meter en paréntesis
print(f'El valor de la suma es: {suma}')
print('_________________________________________')
#Resta
print('RESTA')
